package basic;

public class DataType {

    public static void main(String[] args) {

        byte b = 127;
        b++;
        System.out.println(b);

        double d = 1234.5;
        float f = 1234.5f;
        System.out.println(d);
        System.out.println(f);

        boolean bool = true;
        System.out.println(bool);

        char c = 'A';
        System.out.println(c);

        int i = c;
        System.out.println(i);

    }

}
